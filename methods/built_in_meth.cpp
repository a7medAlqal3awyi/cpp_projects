#include <iostream>
#include <cmath>
using namespace std;
int main()
{
    cout << pow(2, 4) << "\n";
    cout << fmod(10.5, 2) << "\n";
    cout << ceil(10.1) << "\n";
    cout << floor(10.99) << "\n";
    cout << round(10.499) << "\n";
    cout << round(10.5) << "\n";
    cout << trunc(50.5) << "\n";
    cout << trunc(10.9) << "\n";
    cout << trunc(70.1) << "\n";
}
